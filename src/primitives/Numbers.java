package primitives;

public class Numbers {
    public static void main(String[] args) {
        //dataType variableName = value;
        System.out.println("\n-------numbers-byte-short-int-long-------\n");
        byte age = 45;
        long favNumber = 2147483648L;

        //bite -> 1 bite
        // short -> 2 bites
        // int->4 bites
        // long -> 8 bites
        // floating numbers
        // float -> 4 bites
        // double -> 8 bites//
        System.out.println("\n------floating numbers-----float-double------\n");

        float balance1 = 23.746463846284964F;
        double balance2 = 23.746463846284964;
        double price = 23.5;
        System.out.println(balance1);
        System.out.println(balance2);
        System.out.println(price);




        System.out.println(favNumber);
        System.out.println(age);

    }
}
