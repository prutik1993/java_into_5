package escape_sequences;

public class SpecialCharacters {
    public static void main (String[] args){

        System.out.println("apple\\\\\\orange");
        System.out.println("I like \"Sundays\" and apple"); // \"Sundays\"

        System.out.println("My fav fruit \"kiwi\" and \"orange\"");
        System.out.println("My fav fruit \"kiwi\"" + " and \"orange\""); //space between + and""


    }

}
