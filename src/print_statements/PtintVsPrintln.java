package print_statements;

public class PtintVsPrintln {
    public static void main (String[] args) {

        //println
        // println prints the given text and moves cursor to the next line
        System.out.println("This is println");
        System.out.println("Anastasiya Prudnikova");
        System.out.println("Today is Sunday");

        //print
        System.out.print("This is print");
        System.out.print("Anastasiya Prudnikova");
        System.out.print("Today is Sunday");

    }
}
