package loops.do_while_loops;

public class UnderstandingDoWhile {
    public static void main(String[] args) {
        int i = 0;
        do{
            System.out.println(i);
            i++;
        }
        while (i < 5);

        int j = 5;
        while(i < 5){
            System.out.println(j);
            j++;
        }

    }


}
