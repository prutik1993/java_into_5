package variebles;

import java.sql.SQLOutput;

public class UnderstandingVariables {
    public static void main(String[] args){
        // variable declaration and assignment with an initial value
        int  age = 45;
        System.out.println(age); //45

        // declaration reserving space it allocate(reserve) the memory
        String name;
        //Variable assignment
        name = "John";

        System.out.println(name);//john
        //re-assigning
        name = "Mike";
        System.out.println(name);//mike
        age = 35;
        System.out.println(age);




    }
}
